    )

    fun deleteSingle(): FilesViewState = populated().copy(
        deleteDialog = DeleteDialogView(names = listOf("scratch.txt"), count = 1),
    )

    fun deleteBulk(): FilesViewState = populated().copy(
        deleteDialog = DeleteDialogView(
