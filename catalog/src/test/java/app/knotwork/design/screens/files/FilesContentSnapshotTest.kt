import org.junit.Test
import org.junit.runner.RunWith
import org.robolectric.annotation.Config
import org.robolectric.annotation.GraphicsMode

@RunWith(AndroidJUnit4::class)
@GraphicsMode(GraphicsMode.Mode.NATIVE)
@Config(sdk = [36], qualifiers = "w360dp-h760dp-xhdpi")
class FilesContentSnapshotTest {
    @get:Rule
    val composeTestRule = createComposeRule()

    @Test fun files_empty_light() = snapshot("empty", dark = false) { FilesContent(state = FilesPreview.empty()) }

    @Test fun files_empty_dark() = snapshot("empty", dark = true) { FilesContent(state = FilesPreview.empty()) }

    @Test fun files_populated_light() = snapshot("populated", dark = false) {
        FilesContent(state = FilesPreview.populated())
    }

    @Test fun files_populated_dark() = snapshot("populated", dark = true) {
        FilesContent(state = FilesPreview.populated())
    }

    @Test fun files_selection_light() = snapshot("selection", dark = false) {
        FilesContent(state = FilesPreview.selection())
    }

    @Test fun files_selection_dark() = snapshot("selection", dark = true) {
        FilesContent(state = FilesPreview.selection())
    }

    @Test fun files_preview_light() = snapshot("preview", dark = false) { FilesContent(state = FilesPreview.preview()) }

    @Test fun files_preview_dark() = snapshot("preview", dark = true) { FilesContent(state = FilesPreview.preview()) }

    @Test fun files_preview_large_light() =
        snapshot("preview_large", dark = false) { FilesContent(state = FilesPreview.previewLarge()) }

    @Test fun files_error_light() = snapshot("error", dark = false) { FilesContent(state = FilesPreview.error()) }

    @Test fun files_error_dark() = snapshot("error", dark = true) { FilesContent(state = FilesPreview.error()) }

    @Test fun files_quota_near_light() =
        snapshot("quota_near", dark = false) { FilesContent(state = FilesPreview.quotaNear()) }

    @Test fun files_quota_over_light() =
        snapshot("quota_over", dark = false) { FilesContent(state = FilesPreview.quotaOver()) }

    @Test fun files_delete_light() = snapshot("delete", dark = false) {
        FilesContent(state = FilesPreview.deleteSingle())
    }

    @Test fun files_delete_bulk_light() =
        snapshot("delete_bulk", dark = false) { FilesContent(state = FilesPreview.deleteBulk()) }

    @Test fun files_collision_light() = snapshot("collision", dark = false) {
        FilesContent(state = FilesPreview.collision())
    }

    private fun snapshot(name: String, dark: Boolean, content: @Composable () -> Unit) {
        composeTestRule.setContent {
            CompositionLocalProvider(LocalKnotworkA11y provides FixedKnotworkA11y(reducedMotion = true)) {
                KnotworkTheme(darkTheme = dark) { content() }
            }
        }
        val themeTag = if (dark) "dark" else "light"
        composeTestRule.onRoot().captureRoboImage(filePath = "src/test/snapshots/files_${name}_$themeTag.png")
    }
}

