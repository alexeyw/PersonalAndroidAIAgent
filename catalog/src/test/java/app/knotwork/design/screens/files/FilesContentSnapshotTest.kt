    private fun normalQuota() =
        QuotaView(count = 7, usageText = "1.5 MB / 64 MB", fraction = 0.02f, tone = QuotaTone.Normal)

    fun empty(): FilesViewState = FilesViewState(
        visualState = FilesVisualState.Empty,
        quota = QuotaView(count = 0, usageText = "0 B / 64 MB", fraction = 0f, tone = QuotaTone.Normal),
